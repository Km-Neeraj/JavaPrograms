
public class ArithmeticOperatorEx2 {

	public static void main(String[] args) {
		int a;
		a=7/2;
		System.out.println(a);
		a=-7/2;
		System.out.println(a);
		a=7/-2;
		System.out.println(a);
		a=-7/-2;
		System.out.println(a);
		
		a=7%2;
		System.out.println(a);
		a=-7%2;
		System.out.println(a);
		a=7%-2;
		System.out.println(a);
		a=2/7;
		System.out.println(a);
	}

}
